package sistemaVoo.util;

public class Horario {

	private int hora;
	private int min;

	public Horario(int h,int m) {
		this.hora = h;
		this.min = m;
	}

	public int getHora() {
		return hora;
	}

	public void setHora(int hora) {
		this.hora = hora;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}



}
